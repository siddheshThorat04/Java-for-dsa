public class swap {
 public static void main(String[] args) {
    int a=8;
    int b=9;
    int temp;
    System.out.println("a="+a);
    System.out.println("b="+b);
    System.out.println("After swapping");
    temp=a;
    a=b;
    b=temp;

    System.out.println("a="+a);
    System.out.println("b="+b);
 }   
}
